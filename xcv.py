print("Input a number from 1 to 7")
x=input()
x=int(x)
if x==1:
	print("1-Sunday")
elif x==2:
	print("2-Monday")
elif x==3:
	print("3-Tuesday")
elif x==4:
	print("4-Wednesday")
elif x==5:
	print("5-Thursday")
elif x==6:
	print("6-Friday")
elif x==7:
	print("7-Saturday")
else:
	print(str(x)+" is not between 1 and 7.")

	
